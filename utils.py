"""
Moduł z podstawowymi operacjami matematycznymi.
"""


def add(a: int, b: int) -> int:
    """
    Dodaje dwie liczby.
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    Odejmuje dwie liczby.
    """
    return a - b


def multiply(a: int, b: int) -> int:
    """
    Mnoży dwie liczby.
    """
    return a * b


def divide(a: int, b: int) -> float:
    """
    Dzieli dwie liczby.
    """
    return a / b
